
public class AreaCalculator {

	/**
	 * This code computes the area of a rectangle
	 */
	
	public static void main(String[] args) {
		double length = 11, width = 0, area;
		
		area = length / width;
		
		System.out.print("The area of a " + length + " by " + width + " rectangle is " + area);

	}

}
